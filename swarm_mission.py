def run_planner():
    log.info("═" * 62)
    log.info("STEP 1 — Generating missions (main_2_.py) …")
    log.info("═" * 62)
    try:
        subprocess.run([sys.executable, MAIN_SCRIPT], check=True)
        log.info("Mission planner finished ✓")
    except subprocess.CalledProcessError as exc:
        log.error(f"main_2_.py failed with exit code {exc.returncode}")
        sys.exit(1)
    except FileNotFoundError:
        log.error(f"Cannot find {MAIN_SCRIPT} — ensure it is in the same directory")
        sys.exit(1)


def load_missions() -> dict:
    pattern = os.path.join(MISSIONS_DIR, 'drone_*.waypoints')
    files   = sorted(glob.glob(pattern))
    if not files:
        log.error(f"No waypoint files found in {MISSIONS_DIR}/")
        sys.exit(1)
    missions = {}
    for fp in files:
        try:
            idx = int(os.path.basename(fp)
                      .replace('drone_', '').replace('.waypoints', ''))
        except ValueError:
            continue
        wps = parse_waypoint_file(fp)
        if not wps:
            log.warning(f"D{idx}: empty waypoint file — skipping")
            continue
        missions[idx] = wps
        log.info(f"  D{idx}: {len(wps)} waypoints  ← {fp}")
    return missions


def main():
    parser = argparse.ArgumentParser(description='Swarm Mission Planner')
    parser.add_argument(
        '--skip-prearm', action='store_true',
        help='Skip pre-arm sensor checks and force-arm each drone '
             '(useful for SITL or bench testing without full sensor suite)',
    )
    args = parser.parse_args()
    skip_prearm: bool = args.skip_prearm

    if skip_prearm:
        log.warning("⚠  --skip-prearm active: pre-arm checks will be bypassed")

    # Step 1 — generate missions
    run_planner()

    # Step 2 — load waypoint files
    log.info("═" * 62)
    log.info("STEP 2 — Loading waypoint files …")
    log.info("═" * 62)
    missions = load_missions()
    if not missions:
        log.error("No missions found — exiting")
        sys.exit(1)

    # Step 3 — build DroneState objects
    log.info("═" * 62)
    log.info("STEP 3 — Configuring swarm …")
    log.info("═" * 62)
    states: List[DroneState] = []
    for (idx, conn_str, sysid) in DRONES:
        if idx not in missions:
            log.warning(f"D{idx} has no mission file — skipping")
            continue
        s = DroneState(
            index       = idx,
            sysid       = sysid,
            conn_str    = conn_str,
            mission_wps = missions[idx],
        )
        states.append(s)
        log.info(f"  D{idx}  sysid={sysid}  {conn_str}  ({len(missions[idx])} WPs)")

    if not states:
        log.error("No drones configured — check DRONES list and mission files")
        sys.exit(1)

    # Step 4 — launch per-drone threads
    log.info("═" * 62)
    log.info(f"STEP 4 — Launching {len(states)} drone thread(s)  "
             f"DRONE_DELAY={DRONE_DELAY}s  ALT={TAKEOFF_ALT}m")
    log.info("═" * 62)

    threads = []
    for s in states:
        t = threading.Thread(
            target=drone_thread,
            args=(s, states, skip_prearm),
            name=f"D{s.index}",
            daemon=True,
        )
        threads.append(t)
        t.start()
        time.sleep(0.3)   # slight stagger so socket binds don't race

    for t in threads:
        t.join()

    # Summary
    log.info("═" * 62)
    log.info("SWARM COMPLETE")
    log.info("═" * 62)
    for s in states:
        if s.error:
            log.error(f"  D{s.index}  ✗  {s.error}")
        else:
            log.info(f"  D{s.index}  ✓  OK")


if __name__ == '__main__':
    main()