"""
Swarm Mission Planner  (pymavlink only — no dronekit)
======================================================
Executes multi-drone coverage missions generated by main_2_.py.

Pipeline:
  1. Runs main_2_.py  →  produces missions/drone_N.waypoints
  2. Connects to each drone through MAVProxy's UDP --out port
  3. Arms, takes off (staggered by DRONE_DELAY), uploads & executes mission
  4. Obstacle-avoidance hold during transit phase only
  5. Lands via SET_MODE → LAND  (not RTL)

SITL setup (run each block in its own terminal BEFORE this script):
  # Drone 1
  sim_vehicle.py -v ArduCopter -I0 --sysid 1
  mavproxy.py --retries 5 --master tcp:127.0.0.1:5760 --sitl 127.0.0.1:5501 \
              --out 127.0.0.1:14550 --map --console

  # Drone 2
  sim_vehicle.py -v ArduCopter -I1 --sysid 2
  mavproxy.py --retries 5 --master tcp:127.0.0.1:5770 --sitl 127.0.0.1:5511 \
              --out 127.0.0.1:14560 --map --console

  # Then:
  python swarm_mission_planner.py

MAVProxy uses --out without 'udp:' prefix; it pushes UDP packets TO that
address.  pymavlink must therefore LISTEN (server side) using 'udpin:'.

Real hardware: replace conn_str with e.g. '/dev/ttyUSB0,57600'
"""

